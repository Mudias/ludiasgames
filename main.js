const heroImgEl = document.getElementById("heroImg");
const factEl = document.getElementById("fact");
const factBodyEl = document.getElementById("factBody");
const aboutThisGameEl = document.getElementById("aboutThisGame");
const aboutThisGameBodyEl = document.getElementById("aboutThisGameBody");
const featuresEl = document.getElementById("features");
const featuresBodyEl = document.getElementById("featuresBody");

const allAssetsEl = document.getElementById("allAssets");
const screenshotsEl = document.getElementById("screenshots");
const logosEl = document.getElementById("logos");
const steamCapsuleEl = document.getElementById("steamCapsule");
const templatesEl = document.getElementById("templates");
const steamCapsuleImgEl = document.getElementById("steamCapsuleImg");
const fontTitleEl = document.getElementById("fontTitle");
const fontEl = document.getElementById("font");
const logoDarkEl = document.getElementById("logoDark");
const logoLightEl = document.getElementById("logoLight");

const selectedLangEl = document.querySelector(".selected-lang");

// Helper to switch languages
function setLanguage(lang) {
    currentLang = lang;
    titleEl.innerHTML = translations[lang].title;
    heroImgEl.src = translations[lang].heroImg;
    factEl.innerHTML = translations[lang].fact;
    factBodyEl.innerHTML = translations[lang].factBody;
    aboutThisGameEl.innerHTML = translations[lang].aboutThisGame;
    aboutThisGameBodyEl.innerHTML = translations[lang].aboutThisGameBody;
    featuresEl.innerHTML = translations[lang].features;
    featuresBodyEl.innerHTML = translations[lang].featuresBody;

    allAssetsEl.href = translations[lang].allAssetsHref;
    screenshotsEl.href = translations[lang].screenshotsHref;
    logosEl.href = translations[lang].logosHref;
    steamCapsuleEl.href = translations[lang].steamCapsuleHref;
    templatesEl.href = translations[lang].templatesHref;
    steamCapsuleImgEl.src = translations[lang].steamCapsuleSrc;
    fontTitleEl.innerHTML = translations[lang].fontTitle;
    fontEl.href = translations[lang].fontHref;
    fontEl.innerHTML = translations[lang].fontButtonText
    logoDarkEl.src = translations[lang].logoDarkSrc;
    logoLightEl.src = translations[lang].logoLightSrc;

    selectedLangEl.className = `selected-lang ${lang}`;
    localStorage.setItem("lang", lang); // remember language
}

// Handle clicks
document.querySelectorAll(".lang-menu ul li a").forEach(link => {
    link.addEventListener("click", e => {
        e.preventDefault();
        const lang = e.target.classList[0];
        setLanguage(lang);
    });
});

// Load previously saved language
window.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("lang") || "us";
    setLanguage(savedLang);
});

function downloadVideo() {
    const link = document.createElement('a');
    link.href = 'The Sushi Restaurant Announcement Trailer.mp4';
    link.download = 'The Sushi Restaurant Announcement Trailer.mp4';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}